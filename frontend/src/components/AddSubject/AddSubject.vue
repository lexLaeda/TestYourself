<template>
  <div>
    <v-card
        class="mx-auto"
        max-width="700"
        ref="formWrap"
    >
      <v-card-title class="display-1">Добавить предмет</v-card-title>
      <v-card-text>
        <v-form
            ref="form"
            v-model="valid"
        >
          <v-text-field
              v-model="postData.name"
              label="Название"
              :rules="[v => !!v || validText]"
              outlined
              required
          ></v-text-field>

          <v-textarea
              v-model="postData.description"
              label="Описание"
              :rules="[v => !!v || validText]"
              outlined
              required
          ></v-textarea>
          <v-row class="mt-4">
            <v-col cols="12" sm="auto">
              <v-btn large block color="primary darken-2 white--text"
                     :disabled="!valid"
                     @click="saveSubject"
              >Сохранить</v-btn>
            </v-col>
            <v-col cols="12" sm="auto">
              <v-btn large block color="primary darken-2 white--text"
                     :disabled="!valid"
                     @click="addNewSubject"
              >Сохранить и добавить новый</v-btn>
            </v-col>
            <v-col cols="12" sm="auto" class="ml-auto">
              <v-btn large block
                     @click="back"
              >Отменить</v-btn>
            </v-col>
          </v-row>
        </v-form>
      </v-card-text>
    </v-card>
  </div>
</template>

<script>
  import api from "../../backend-api";

  export default {
    name: 'AddSubject',

    data: () => ({
      valid: true,
      validText: 'Обязательное поле',
      postData: {
        num_q: 0
      }
    }),

    methods: {
      sendSubject() {
        this.$refs.form.validate();
        let postData = JSON.stringify(this.postData);
        api.addSubject(postData)
          .then(function (response) {
            console.log('ADD-SUBJECT =>', response);
          })
          .catch(function (error) {
            console.log(error);
          });
      },

      back() {
        this.$router.go(-1);
      },

      saveSubject() {
        this.sendSubject();
        this.back();
      },

      addNewSubject() {
        this.sendSubject();
        this.$refs.form.reset();
        this.$vuetify.goTo(this.$refs.formWrap);
      }
    }
  }
</script>